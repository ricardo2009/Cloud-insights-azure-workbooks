

Observações da referencia:

Todas as tabelas desse workbook contem as seguintes configurações que devem ser aplicadas no nosso workbook:

- No nome do recurso é colocado um link onde se o se o usuario clicar no nome do recurso ele já é direcionado para a página do recurso no portal do Azure. depois ele consegue volta para o dashboard
  isso precisa ser aplicado em todas as tabelas do nosso workbook, sem excessão.
- Formatação condicional em tabelas com valores de performance, utilizando "verde,amarelo,vermelho", sendo "verde=bom","amarelo=atenção","vermelho=risco ou alto pico de demanda"
- Configuração de exportar para um excel, esse botão está disponivél em todas as tabelas.



